#!/usr/bin/env python3
"""
Module for converting a floating-point number to its string representation.
This module defines a function `to_str` that takes a float and returns it as a string.
"""

def to_str(n: float) -> str:
    """
    Converts a floating-point number to its string representation.

    Args:
        n (float): The floating-point number to be converted.

    Returns:
        str: The string representation of the given float.
    """
    return str(n)

