#!/usr/bin/env python3
"""
Basic function with type annotations.
This module defines a function `add` that adds two floating-point numbers.
"""

def add(a: float, b: float) -> float:
    """
    Adds two float values and returns their sum.

    Args:
        a (float): The first number to add.
        b (float): The second number to add.

    Returns:
        float: The sum of a and b.
    """
    return a + b

