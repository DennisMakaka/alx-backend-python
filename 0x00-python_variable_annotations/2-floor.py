#!/usr/bin/env python3
"""
Module for calculating the floor of a floating-point number.
This module defines a function `floor` that converts a float to an integer.
"""

def floor(n: float) -> int:
    """
    Returns the floor of a floating-point number by converting it to an integer.

    Args:
        n (float): The floating-point number to be floored.

    Returns:
        int: The largest integer less than or equal to the given float.
    """
    return int(n)

